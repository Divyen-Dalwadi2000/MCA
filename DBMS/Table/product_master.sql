CREATE TABLE PRODUCT_MASTER(
	PRODUCT_ID VARCHAR(6) PRIMARY KEY,
    NAME VARCHAR(30) NOT NULL,
    COMPANY_ID VARCHAR(6),
    CATEGORY_ID VARCHAR(6),
    SCHEME_ID VARCHAR(6),
    COST DECIMAL(7,2),
    QTY_IN_HAND INTEGER(3),
    FOREIGN KEY (COMPANY_ID) REFERENCES COMPANY_MASTER(COMPANY_ID),
    FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORY_MASTER(CATEGORY_ID),
    FOREIGN KEY (SCHEME_ID) REFERENCES SCHEME_MASTER(SCHEME_ID)
);

